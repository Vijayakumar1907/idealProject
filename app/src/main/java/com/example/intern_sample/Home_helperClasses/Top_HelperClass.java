package com.example.intern_sample.Home_helperClasses;

public class Top_HelperClass {

    int image_top;
    String img_text;

    public Top_HelperClass(int image_top, String img_text) {
        this.image_top = image_top;
        this.img_text = img_text;
    }

    public int getImage_top() {
        return image_top;
    }

    public String getImg_text() {
        return img_text;
    }
}
